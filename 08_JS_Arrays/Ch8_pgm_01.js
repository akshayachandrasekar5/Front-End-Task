
// Creating arrays

var scores;
var names;

scores = [3, 1, 8, 2];
names = ["Kandra", "Dax", "Blinky"];

console.log(scores);
console.log(names);

// Further Adventures

// 1) Add an extra element to each array.
scores.push(5); // Adds 5 to the scores array
names.push("Akshaya"); // Adds "" to the names array

console.log(scores);
console.log(names);

// 2) Declare a third variable, akaScores.
var akaScores;

// 3) Assign the scores array to akaScores.
akaScores = scores;

// 4) Log akaScores to the console.
console.log(akaScores);
